function OpenStatusBadge({ status }: { status: OpenStatus }) {
  if (status === 'open') {
    return (
      <span className="inline-flex items-center gap-1 px-2 py-0.5 rounded-full bg-emerald-100 text-emerald-700 text-xs font-medium">
        <span className="w-1.5 h-1.5 bg-emerald-500 rounded-full animate-pulse"></span>
        Open
      </span>
    );
  }
  return (
    <span className="inline-flex items-center gap-1 px-2 py-0.5 rounded-full bg-slate-100 text-slate-500 text-xs font-medium">
      Hours unknown
    </span>
  );
}

// Match probability badge with confidence indicator (Bayesian ranking)
function MatchProbabilityBadge({ place }: { place: Place }) {
  if (place.probability === undefined) return null;
  
  const confidenceLevel = getConfidenceLevel(place.confidence);
  const probability = formatProbability(place.probability);
  const interval = formatCredibleInterval(place.p10, place.p90);
  
  // Color based on confidence level
  const confidenceColors = {
    high: 'bg-emerald-100 text-emerald-700 border-emerald-200',
    medium: 'bg-amber-100 text-amber-700 border-amber-200',
    low: 'bg-slate-100 text-slate-600 border-slate-200',
  };
  
  const confidenceLabels = {
    high: 'High confidence',
    medium: 'Medium confidence',
    low: 'Low confidence',
  };
  
  return (
    <div className="flex items-center gap-2">
      {/* Main probability badge */}
      <span className={`inline-flex items-center gap-1.5 px-2.5 py-1 rounded-lg border text-xs font-semibold ${confidenceColors[confidenceLevel]}`}>
        <svg className="w-3.5 h-3.5" fill="currentColor" viewBox="0 0 20 20">
          <path fillRule="evenodd" d="M3 3a1 1 0 000 2v8a2 2 0 002 2h2.586l-1.293 1.293a1 1 0 101.414 1.414L10 15.414l2.293 2.293a1 1 0 001.414-1.414L12.414 15H15a2 2 0 002-2V5a1 1 0 100-2H3zm11.707 4.707a1 1 0 00-1.414-1.414L10 9.586 8.707 8.293a1 1 0 00-1.414 0l-2 2a1 1 0 101.414 1.414L8 10.414l1.293 1.293a1 1 0 001.414 0l4-4z" clipRule="evenodd" />
        </svg>
        Match: {probability}
      </span>
      
      {/* Confidence tooltip */}
      <span 
        className="text-xs text-slate-400 cursor-help"
        title={`${confidenceLabels[confidenceLevel]}: ${interval}`}
      >
        {interval && `(${interval})`}
      </span>
    </div>
  );
}

// Use reasons directly from the ranking engine (already prioritized)
function getReasonChips(place: Place): string[] {
  // Reasons are already generated by the ranking engine with priority
  // Just return up to 3 chips
  return place.reasons.slice(0, 3);
}

// Compact reason chips component
function ReasonChips({ place }: { place: Place }) {
  const chips = getReasonChips(place);
  
  if (chips.length === 0) return null;
  
  return (
    <div className="flex flex-wrap gap-1.5 pt-3 border-t border-slate-100">
      {chips.map((chip, i) => (
        <span
          key={i}
          className="inline-flex items-center px-2 py-0.5 rounded-md bg-slate-100 text-slate-600 text-[11px] font-medium"
        >
          {chip}
        </span>
      ))}
    </div>
  );
}

function RankedPlaceCard({ 
  place, 
  rank,
  isSelected, 
  onClick 
}: { 
  place: Place; 
  rank: number;
  isSelected: boolean;
  onClick: () => void;
}) {
  const getCategoryGradient = (category: string) => {
    switch (category) {
      case 'restaurant': return 'from-orange-500 to-amber-500';
      case 'cafe': return 'from-amber-500 to-yellow-500';
      case 'grocery': return 'from-sky-500 to-blue-500';
      case 'scenic': return 'from-emerald-500 to-teal-500';
      case 'indoor': return 'from-purple-500 to-indigo-500';
      default: return 'from-blue-500 to-indigo-500';
    }
  };

  const getGlowClass = (category: string) => {
    switch (category) {
      case 'restaurant': return 'glow-orange';
      case 'cafe': return 'glow-amber';
      case 'grocery': return 'glow-sky';
      case 'scenic': return 'glow-green';
      case 'indoor': return 'glow-purple';
      default: return 'glow-blue';
    }
  };

  const isTopPick = rank === 1;

  return (
    <div
      onClick={onClick}
      className={`relative overflow-hidden rounded-2xl cursor-pointer transition-all duration-300 ${
        isSelected ? `${getGlowClass(place.category)} scale-[1.02]` : 'hover:scale-[1.01] hover:shadow-xl'
      }`}
    >
      {/* Gradient header */}
      <div className={`bg-gradient-to-r ${getCategoryGradient(place.category)} ${isTopPick ? 'p-5' : 'p-4'} text-white`}>
        <div className="flex items-start justify-between">
          <div className="flex-1">
            {isTopPick && (
              <div className="flex items-center gap-2 mb-1">
                <span className="inline-flex items-center gap-1 px-2 py-0.5 bg-white/20 backdrop-blur rounded-full text-xs font-medium">
                  <svg className="w-3 h-3" fill="currentColor" viewBox="0 0 20 20">
                    <path d="M9.049 2.927c.3-.921 1.603-.921 1.902 0l1.07 3.292a1 1 0 00.95.69h3.462c.969 0 1.371 1.24.588 1.81l-2.8 2.034a1 1 0 00-.364 1.118l1.07 3.292c.3.921-.755 1.688-1.54 1.118l-2.8-2.034a1 1 0 00-1.175 0l-2.8 2.034c-.784.57-1.838-.197-1.539-1.118l1.07-3.292a1 1 0 00-.364-1.118L2.98 8.72c-.783-.57-.38-1.81.588-1.81h3.461a1 1 0 00.951-.69l1.07-3.292z" />
                  </svg>
                  Top Pick
                </span>
              </div>
            )}
            <h3 className={`font-bold ${isTopPick ? 'text-xl' : 'text-lg'}`}>{place.name}</h3>
            <p className="text-white/80 text-sm">{place.type}</p>
          </div>
        </div>
      </div>

      {/* Content */}
      <div className={`bg-white ${isTopPick ? 'p-5' : 'p-4'}`}>
        {/* Stats */}
        <div className="flex items-center gap-4 mb-3">
          <div className="flex items-center gap-1.5 text-slate-600">
            <svg className="w-4 h-4 text-slate-400" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17.657 16.657L13.414 20.9a1.998 1.998 0 01-2.827 0l-4.244-4.243a8 8 0 1111.314 0z" />
            </svg>
            <span className="font-semibold text-sm">{place.distanceKm} km</span>
          </div>
          <div className="flex items-center gap-1.5 text-slate-600">
            <svg className="w-4 h-4 text-slate-400" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 8v4l3 3m6-3a9 9 0 11-18 0 9 9 0 0118 0z" />
            </svg>
            <span className="font-semibold text-sm">{place.walkMins} min</span>
          </div>
          <OpenStatusBadge status={place.openStatus} />
          {place.vegFriendly && (
            <span className="inline-flex items-center gap-1 text-emerald-600 font-medium text-sm">
              ðŸ¥¬ Veg
            </span>
          )}
        </div>

        {/* Match Probability (Bayesian ranking) */}
        {place.probability !== undefined && (
          <div className="mb-3">
            <MatchProbabilityBadge place={place} />
          </div>
        )}

        {/* Tags */}
        {place.tags.length > 0 && (
          <div className="flex flex-wrap gap-1.5 mb-3">
            {place.tags.map((tag, i) => (
              <span key={i} className="text-xs px-2.5 py-1 bg-slate-100 text-slate-600 rounded-lg">
                {tag}
              </span>
            ))}
          </div>
        )}

        {/* Reason Chips - compact premium UI */}
        <ReasonChips place={place} />
      </div>
    </div>
  );
}

